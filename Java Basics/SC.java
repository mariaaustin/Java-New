public class SC {
    public static void main(String[] args) {
        float ys=1000000; //yearly salary
        float ms=ys/12; //monthy salary
        float ws=ms/4; //weekly salary
        float ds=ws/7; //daily salary
        float hs=ds/24; //hourly salary
        System.out.println("The monthy salary is :" + ms);
        System.out.println("The weekly salary is :" + ws);
        System.out.println("The daily salary is :" + ds);
        System.out.println("The hourly salary is :" + hs);
    }

    
}
