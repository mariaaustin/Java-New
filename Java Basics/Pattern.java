public class Pattern {
    public static void main(String[] args) {
        char k='A';
        for(char i='A';i<='E';i++)
        {
            for(char j='A'; j<='E';j++)
            {
                System.out.print(k);
                k++;
            }
            System.out.println("");
            
        }
    }
    
}
